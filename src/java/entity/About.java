package entity;

/**
 *
 * @author bingu
 */
public class About {
    private int id;
    private String detail;
    private String image;
    private Boolean status;

    public About(int id, String detail, String image, Boolean status) {
        this.id = id;
        this.detail = detail;
        this.image = image;
        this.status = status;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getDetail() {
        return detail;
    }

    public void setDetail(String detail) {
        this.detail = detail;
    }

    public String getImage() {
        return image;
    }

    public void setImage(String image) {
        this.image = image;
    }

    public Boolean getStatus() {
        return status;
    }

    public void setStatus(Boolean status) {
        this.status = status;
    }
    
    
    
}
